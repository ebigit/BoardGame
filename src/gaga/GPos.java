package gaga;

/**
 * 位置を示す列挙型です。<br>
 * CENTER … コンテンツの中央<br>
 * TOP … コンテンツの上辺中央<br>
 * RIGHT … コンテンツの右辺中央<br>
 * BOTTOM … コンテンツの下辺中央<br>
 * LEFT … コンテンツの左辺中央<br>
 * TOP_RIGHT … コンテンツの右上角<br>
 * BOTTOM_RIGHT … コンテンツの右下角<br>
 * BOTTOM_LEFT … コンテンツの左下角<br>
 * TOP_LEFT … コンテンツの左上角<br>
 */
public enum GPos {
	CENTER, TOP, TOP_RIGHT, RIGHT, BOTTOM_RIGHT, BOTTOM, BOTTOM_LEFT, LEFT, TOP_LEFT
}
